let valor = Math.PI;

//Arrendondar
console.log(Math.round(valor));

//Retorna o piso do numero
console.log(Math.floor(valor));

//Retorna o teto do numero
console.log(Math.ceil(valor));

//Retorna o modulo do numero
console.log(Math.abs(valor * -1));

//Retorna o menor valor
console.log(Math.min(1, 2, 3));

//Retorna o maior valor
console.log(Math.max(1, 2, 3));

//Retorna numero aleatorio entre 0 e 1
console.log(Math.random());